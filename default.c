typedef struct {
  uint16_t code;
  const char* img;
} TUcode_t;

// for the binary search algorithm, the value of 'code' should follow in ascending order!
static TUcode_t fontX[] = {
  { 0x0030, f_0x30 }, // 0
  { 0x0031, f_0x31 }, // 1
  { 0x0032, f_0x32 }, // 2
  { 0x0033, f_0x33 }, // 3
  { 0x0034, f_0x34 }, // 4
  { 0x0035, f_0x35 }, // 5
  { 0x0036, f_0x36 }, // 6
  { 0x0037, f_0x37 }, // 7
  { 0x0038, f_0x38 }, // 8
  { 0x0039, f_0x39 }, // 9
  { 0x003f, f_0x3F }, // ?
  { 0x0041, f_0x41 }, // A
  { 0x0046, f_0x46 }, // F
  { 0x0065, f_0x65 }, // e
  { 0x006c, f_0x6C }, // l
};

// through the binary search algorithm
const char* getFontX( uint16_t key) {
    int len = sizeof( fontX ) / sizeof ( TUcode_t );
    int found = 0;
    int high = len - 1, low = 0;
    int middle = (high + low) / 2;
    while ( !found && high >= low ){
        if ( key == fontX[middle].code ) {
            found = 1;
            break;
        } else if (key < fontX[middle].code ) {
            high = middle - 1;
        } else {
            low = middle + 1;
        }
        middle = (high + low) / 2;
    }
    // if not found, then return symbol '?'
    return (found == 1) ? fontX[middle].img : f_0x3F ;
}

